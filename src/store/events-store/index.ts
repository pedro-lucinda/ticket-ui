import { create } from 'zustand'

export type IEvent = {
  title: string
  date: string
  description: string
  id: string
  image: string
  city: string
  state: string
  country: string
  address: string
  price: number
  currency: 'USD'
}

export type EventState = {
  events: IEvent[] | null
  getEvent: (id: string) => Promise<IEvent | undefined>
}

export const useEventStore = create<EventState>(
  (): EventState => ({
    events: mock,
    getEvent: async (id: string) => onGetEvent(id),
  }),
)

async function onGetEvent(id: string) {
  const events = useEventStore.getState().events
  return events?.find((event) => event.id === id)
}
